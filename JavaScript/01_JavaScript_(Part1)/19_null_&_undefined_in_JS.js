/*
********************** undefined ******************************
A variable that has not been assigned a value is of type undefined.

example: 
let a;          // here is 'a' is undefined
 
*/


/*
****************************** null **********************************
The null value represents the intentional absence of any object value.

To be explicitly assigned. ('null' ham jaan buj kar lagate hai taki 'undefined' naa show kare)

Example:
let a = null;          // here 'a' assigned with 'null' value so that it should not show 'undefined'
*/