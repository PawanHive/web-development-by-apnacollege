//Operators:
let a = 10;
let b = 5;

// Arithmetic Operators (+, -, *, /, %, **)
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);
console.log(a**b);

// Unary Operators (++, --):
console.log(a++);
console.log(++a);
console.log(a--);
console.log(--a);

// Assignment Operators (=, +=, -=, *=, /=, %= etc):
b = a;
console.log(b);
console.log(a += 2);
console.log(a -= 2);
console.log(a *= 2); // here value of 'a' becomes 20
console.log(a /= 10);
console.log(a %= 2);
